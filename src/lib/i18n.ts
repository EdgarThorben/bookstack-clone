export type TranslationKey = keyof typeof dict.en;

export function t(lang: Lang, key: TranslationKey): string {
  return dict[lang][key] ?? dict.en[key] ?? key;
}

// Item/relationship types and collection categories are fixed, known enums
// (see src/db/schema.ts), so they get real translations instead of falling
// back to formatItemLabel.
export function tItemType(lang: Lang, type: string): string {
  const key = `itemType.${type}` as TranslationKey;
  return dict[lang][key] ?? dict.en[key] ?? type;
}

export function tRelationshipType(lang: Lang, type: string): string {
  const key = `relationshipType.${type}` as TranslationKey;
  return dict[lang][key] ?? dict.en[key] ?? type;
}
